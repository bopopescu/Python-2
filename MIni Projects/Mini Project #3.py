# For Loops
for W in "Will":
    print(W)
# For Loops run threw an entire word or list

Names = ["Meme", "Carlton", "Wilford", "Lisa"]
for X in Names:
    print("Hello " + X)


# Nested Loops
Colors = ["Red", "Yellow", "Blue", "Black", "Gray", "Green"]
Fruits = ["Apple", "Banana", "Blueberry", "Rotten", "Gris", "Green Apple"]

for x in Colors:
    for y in Fruits:
        print(x + " and " + y)
# It will combined everything wit the first Array of words with the second.